package com.university.hafsa.model.entity;

public class Lesson {
    private int id;
    private String lesson;

    public Lesson() {}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLesson() {
        return lesson;
    }

    public void setLesson(String lesson) {
        this.lesson = lesson;
    }
}
